C ODDEVE.FOR- SELECTS ODD OR EVEN RECORD-SETS OF A FILE

	SUBROUTINE ODDEVE

	COMMON/DEV/ITI

	COMMON IFLAG1,IFLAG2

	INTEGER*4, SAVE :: K

	DATA IE,IIE/'E','e'/

      COMMON /SELGVS/ ISEL

	IF(IFLAG1)10,20,20

 10   K=1

!	WRITE(*,201)
!
! 201  FORMAT('$EVENS(E) OR ODDS(O)? >',\)
!
!	READ(*,101)KFLAG
!
! 101  FORMAT(A1)
!
!      IF(KFLAG-IIE)8,12,8
!
! 8    IF(KFLAG-IE)11,12,11
      IF(ISEL.EQ.0)GOTO 12  ! even selected
      
 11   K=-1

 12   RETURN

 20   K=-K

	IFLAG2=K

	RETURN

	END

