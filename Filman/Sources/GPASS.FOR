C GPASS.FOR- PASSES ALL GROUP VARS TO OUTPUT FILE

	SUBROUTINE GPASS

	INCLUDE 'MAX.INC'
	COMMON IFLAG1
	COMMON/FLDES/ID(8),IBUF(IOMAX)
	COMMON/FLDESO/IDO(8),IBUFO(IOMAX)
	EQUIVALENCE (ID(1),NG),(IDO(1),NGO)

	IF (IFLAG1) 20,10,31
20    IF (NG-2) 21,21,22
22    NGO=NG
	N=6*NG
	J=121
	DO 40 I=13,N  !Copy GV labels across (except 1 and 2)
	IBUFO(J)=IBUF(I)
40    J=J+1
21    RETURN
10    IF (NG-2) 31,31,32
32    DO 30 I=3,NG  ! GROUP VARS 1 AND 2 NOW HANDLED BY FILMAN
30    IBUFO(I)=IBUF(I)
31    RETURN
	END

