C MACHINE.FOR- SUBROUTINES FOR DETERMINING MACHINE-DEPENDENT CONSTANTS

C USED BY VARIOUS NUMERICAL-ANALYSIS ROUTINES VIA INVOCATION OF

C MICROSOFT FORTRAN 5.1 LIBRARY FUNCTIONS.

C MACHINE1.FOR- GETS SINGLE-PRECISION CONSTANTS

      SUBROUTINE MACHINE1(BASE,ETA,INFINY,SMALNO)

      REAL*4 ETA,INFINY,SMALNO,BASE

      BASE=2.0

      ETA=EPSILON(ETA)

      INFINY=HUGE(INFINY)

      SMALNO=TINY(SMALNO)

      RETURN

      END



C MACHINE2.FOR- GETS DOUBLE-PRECISION CONSTANTS 

      SUBROUTINE MACHINE2(BASE,ETA,INFINY,SMALNO)

      REAL*8 ETA,INFINY,SMALNO,BASE

      BASE=2.D0

      ETA=EPSILON(ETA)

      INFINY=HUGE(INFINY)

      SMALNO=TINY(SMALNO)

      RETURN

      END



