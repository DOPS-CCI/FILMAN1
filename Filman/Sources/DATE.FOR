	

C       SUBROUTINE DATE: READS THE CURRENT DATE AND        

C        CONVERTS THE DATE TO A STRING FORMAT(IDATE)

	

	SUBROUTINE DATE(IDATE)





	INTEGER*2 IMON,IDAY,IYR,I,R,S,T

	CHARACTER*1 IDATE(10)

	



	CALL GETDAT(IYR,IMON,IDAY)

     

	R = MOD(IMON,10)

	I=IMON/10

	 

	IDATE(1)=CHAR(I+48)

	IDATE(2)=CHAR(R+48)

	IDATE(3)='-'





	I=IDAY/10

	R=MOD(IDAY,10)



	IDATE(4)=CHAR(I+48)

	IDATE(5)=CHAR(R+48)

	IDATE(6)='-'



	I=IYR/1000

	R=MOD(IYR,1000)

	S=R/100

	R=MOD(R,100)

	T=R/10

	R=MOD(R,10)



	IDATE(7)=CHAR(I+48)

	IDATE(8)=CHAR(S+48)

	IDATE(9)=CHAR(T+48)

	IDATE(10)=CHAR(R+48)

	 

	WRITE(*,*)IDATE

	

	END

