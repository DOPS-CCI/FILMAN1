C SPLTHF.FOR- SELECTS HALF, ODD REC-SET TO FIRST HALF

	SUBROUTINE SPLTHF

	COMMON IFLAG1,IFLAG2,IFLAG3,KNT

 	COMMON /FLDES/NG,NA,NC,ND,NF,NP,NR

	INTEGER*4, SAVE :: NHALF, MID

      COMMON /SELGVS/ ISEL

	IF(IFLAG1)10,20,30

 10   CONTINUE
! 10   WRITE(*,201)
!
! 201  FORMAT('WHICH HALF, 1 OR 2? >',\)
!
!	READ(*,101) NHALF
!
! 101  FORMAT(I1)
      NHALF=ISEL+1

	MID=(NR/NC+1)/2

	RETURN

 20   IF(NHALF.EQ.2) GO TO 25

      IF(KNT.LE.MID) RETURN

      KNT=-1

	GO TO 26

 25   IF(KNT.GT.MID) RETURN

 26   IFLAG2=0

 30   RETURN

	END

